def _collect_stage_data(
    systems_root: str | os.PathLike,
    stage: str,
    gmx_binary: str,
) -> tuple[dict[str, dict[str, str]], list[str]]:
    """Return (by_system, missing_systems) for one stage."""
    rel_path, fmt = _STAGE_SOURCES[stage]
    by_system: dict[str, dict[str, str]] = {}
    missing: list[str] = []

    systems_root = str(systems_root)
    for entry in sorted(os.listdir(systems_root)):
        system_dir = os.path.join(systems_root, entry)
        if not os.path.isdir(system_dir):
            continue
        source = os.path.join(system_dir, rel_path)
        if not os.path.exists(source):
            missing.append(entry)
            continue
        try:
            if fmt == "mdp":
                by_system[entry] = _parse_mdp_file(source)
            else:
                by_system[entry] = _dump_tpr_inputrec(source, gmx_binary)
        except (FileNotFoundError, RuntimeError):
            missing.append(entry)

    return by_system, missing


def _audit_stage(
    stage: str,
    by_system: dict[str, dict[str, str]],
    missing_systems: list[str],
    skip_keys: frozenset[str],
) -> MDPStageAudit:
    """Compute canonical values and deviations for one stage."""
    if not by_system:
        return MDPStageAudit(
            stage=stage, n_systems=0,
            canonical=MappingProxyType({}),
            deviations=(), missing_systems=tuple(sorted(missing_systems)),
        )

    # Collect all values per key across systems
    all_keys: set[str] = set()
    for kv in by_system.values():
        all_keys.update(kv)

    canonical: dict[str, str] = {}
    deviations: list[MDPDeviation] = []

    for key in sorted(all_keys):
        if key in skip_keys:
            continue
        values = [kv[key] for kv in by_system.values() if key in kv]
        if not values:
            continue
        canon = _mode(values)
        canonical[key] = canon
        for system, kv in by_system.items():
            v = kv.get(key)
            if v is not None and v != canon:
                deviations.append(MDPDeviation(
                    system=system, stage=stage, key=key, value=v, canonical=canon,
                ))

    return MDPStageAudit(
        stage=stage,
        n_systems=len(by_system),
        canonical=MappingProxyType(canonical),
        deviations=tuple(deviations),
        missing_systems=tuple(sorted(missing_systems)),
    )


def diff_mdps(
    systems_root: str | os.PathLike,
    *,
    stages: tuple[str, ...] = ("run", "equilibration", "minimization"),
    gmx_binary: str = "gmx",
    skip_keys: frozenset[str] = _DEFAULT_SKIP_KEYS,
) -> MDPAuditReport:
    """Audit MDP parameters across all systems under *systems_root*.

    For 'run' reads run.mdp directly; for 'equilibration' / 'minimization'
    recovers inputrec from the matching .tpr via ``gmx dump -s``.

    Returns an MDPAuditReport with per-stage canonical values and deviations.
    """
    stage_audits: dict[str, MDPStageAudit] = {}
    for stage in stages:
        by_system, missing = _collect_stage_data(systems_root, stage, gmx_binary)
        stage_audits[stage] = _audit_stage(stage, by_system, missing, skip_keys)

    return MDPAuditReport(stages=MappingProxyType(stage_audits))


