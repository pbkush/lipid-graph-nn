from typing import Mapping, Optional, Sequence

_DEFAULT_SKIP_KEYS: frozenset[str] = frozenset({
    "gen-seed", "ld-seed", "tinit", "init-step", "simulation-part",
})

_STAGE_SOURCES: dict[str, tuple[str, str]] = {
    "run":           ("run.mdp",                           "mdp"),
    "equilibration": ("equilibration/martini_eq.tpr",      "tpr"),
    "minimization":  ("minimization/martini_em.tpr",       "tpr"),
}


# ---------------------------------------------------------------------------
# Internal parsing helpers
# ---------------------------------------------------------------------------

def _canonicalise_key(key: str) -> str:
    """Lowercase and replace underscores with hyphens to unify mdp vs tpr key styles."""
    return key.strip().lower().replace("_", "-")


def _normalise_value(value: str) -> str:
    """Strip inline comments and collapse whitespace."""
    value = value.split(";")[0]
    return " ".join(value.split())


def _parse_mdp_file(path: str | os.PathLike) -> dict[str, str]:
    """Parse a GROMACS .mdp file into {canonical_key: normalised_value}."""
    result: dict[str, str] = {}
    with open(path) as fh:
        for line in fh:
            line = line.split(";")[0].strip()
            if not line or "=" not in line:
                continue
            key, _, value = line.partition("=")
            ck = _canonicalise_key(key)
            cv = _normalise_value(value)
            if ck:
                result[ck] = cv
    return result


def _dump_tpr_inputrec(tpr_path: str | os.PathLike, gmx_binary: str = "gmx") -> dict[str, str]:
    """Extract inputrec key-value pairs from a .tpr via `gmx dump -s`.

    Skips matrix/array-structured entries (lines without `=` on the value side,
    or whose key contains `(`).
    """
    try:
        proc = subprocess.run(
            [gmx_binary, "dump", "-s", str(tpr_path)],
            capture_output=True, text=True, timeout=60,
        )
    except FileNotFoundError as exc:
        raise FileNotFoundError(
            f"GROMACS binary {gmx_binary!r} not found. "
            "Provide the correct binary via gmx_binary=."
        ) from exc
    if proc.returncode != 0:
        raise RuntimeError(
            f"gmx dump failed for {tpr_path} (exit {proc.returncode}):\n{proc.stderr[:2000]}"
        )

    result: dict[str, str] = {}
    in_inputrec = False
    for line in proc.stdout.splitlines():
        if line.startswith("inputrec:"):
            in_inputrec = True
            continue
        if in_inputrec:
            if line and not line[0].isspace():
                break
            stripped = line.strip()
            if not stripped or "=" not in stripped:
                continue
            key_part = stripped.split("=")[0]
            if "(" in key_part or "[" in key_part:
                continue
            key, _, value = stripped.partition("=")
            ck = _canonicalise_key(key)
            cv = _normalise_value(value)
            if ck:
                result[ck] = cv
    return result


def _mode(values: Sequence[str]) -> str:
    """Return the most-frequent value; tie broken by first appearance."""
    counter: Counter[str] = Counter()
    first_seen: dict[str, int] = {}
    for i, v in enumerate(values):
        counter[v] += 1
        if v not in first_seen:
            first_seen[v] = i
    return min(counter, key=lambda v: (-counter[v], first_seen[v]))


