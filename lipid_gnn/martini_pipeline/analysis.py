@dataclass(frozen=True)
class MDPDeviation:
    system: str
    stage: str
    key: str
    value: str
    canonical: str


@dataclass(frozen=True)
class MDPStageAudit:
    stage: str
    n_systems: int
    canonical: Mapping[str, str]
    deviations: tuple[MDPDeviation, ...]
    missing_systems: tuple[str, ...]


@dataclass(frozen=True)
class MDPAuditReport:
    stages: Mapping[str, MDPStageAudit]

    @property
    def total_deviations(self) -> int:
        return sum(len(s.deviations) for s in self.stages.values())

    def to_freeze_json(self) -> str:
        """Machine-readable freeze record: {stage: {key: canonical_value}}."""
        freeze = {
            stage: dict(audit.canonical)
            for stage, audit in self.stages.items()
        }
        return json.dumps(freeze, indent=2, sort_keys=True)

    def to_markdown(self) -> str:
        lines: list[str] = [
            "# MDP Audit Report",
            "",
            "Auto-generated by `scripts/simulation/audit_mdps.py`.",
            f"Total unexpected deviations: **{self.total_deviations}**.",
            "",
        ]
        for stage, audit in self.stages.items():
            lines += [
                f"## Stage: `{stage}`",
                "",
                f"- Systems contributing: {audit.n_systems}",
                f"- Missing: {len(audit.missing_systems)}",
                f"- Deviations (excluding skip-keys): {len(audit.deviations)}",
                "",
            ]
            if audit.missing_systems:
                lines.append("**Missing systems:**")
                for s in audit.missing_systems:
                    lines.append(f"- `{s}`")
                lines.append("")

            lines += ["### Canonical parameter values", ""]
            lines += ["| Key | Value |", "| --- | --- |"]
            for k, v in sorted(audit.canonical.items()):
                lines.append(f"| `{k}` | `{v}` |")
            lines.append("")

            if audit.deviations:
                lines += ["### Deviations from canonical", ""]
                lines += ["| System | Key | Value | Canonical |", "| --- | --- | --- | --- |"]
                for d in audit.deviations:
                    lines.append(f"| `{d.system}` | `{d.key}` | `{d.value}` | `{d.canonical}` |")
                lines.append("")
            else:
                lines += ["### Deviations from canonical", "", "None.", ""]

        lines += _cross_stage_diff(self)
        return "\n".join(lines)


def _cross_stage_diff(report: MDPAuditReport) -> list[str]:
    """Produce a section comparing canonical values across stages for shared keys."""
    stage_names = list(report.stages)
    if len(stage_names) < 2:
        return []

    # Collect all keys appearing in at least 2 stages
    key_to_stages: dict[str, list[str]] = {}
    for stage, audit in report.stages.items():
        for k in audit.canonical:
            key_to_stages.setdefault(k, []).append(stage)
    shared_keys = sorted(k for k, stages in key_to_stages.items() if len(stages) >= 2)

    if not shared_keys:
        return []

    lines: list[str] = [
        "## Cross-stage parameter comparison",
        "",
        "Keys shared across at least two stages. Bold = value differs from the first stage.",
        "",
    ]
    header = "| Key | " + " | ".join(f"`{s}`" for s in stage_names) + " |"
    separator = "| --- |" + " --- |" * len(stage_names)
    lines += [header, separator]

    for key in shared_keys:
        values = []
        first_val = None
        for stage in stage_names:
            v = report.stages[stage].canonical.get(key, "—")
            if first_val is None and v != "—":
                first_val = v
            values.append(v)

        differs_any = len({v for v in values if v != "—"}) > 1
        row_vals = []
        for v in values:
            if differs_any and v not in ("—", first_val):
                row_vals.append(f"**`{v}`**")
            else:
                row_vals.append(f"`{v}`")

        lines.append("| `" + key + "` | " + " | ".join(row_vals) + " |")

    lines.append("")
    return lines


